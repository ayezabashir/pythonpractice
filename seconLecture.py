# if condition syntax
a = 14
b = 12
if a > b:
    print('a is greater than b')

# ternary operator
print("a") if a > b else print("b")

#  id else if and else syntax
c = 15
d = 15
if c > d:
    print('c is greater than d')
elif d > c:
    print('d is greater than c')
else:
    print('c is equal to d')

#  loops
#  python has two loops "while" and "for"
#  while loop
i = 1
while i < 6:
    print(i)
    i += 1
